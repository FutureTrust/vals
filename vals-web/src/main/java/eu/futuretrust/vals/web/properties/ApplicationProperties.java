package eu.futuretrust.vals.web.properties;

import org.springframework.boot.context.properties.ConfigurationProperties;
import org.springframework.context.annotation.Configuration;
import org.springframework.context.annotation.PropertySource;

@Configuration
@PropertySource("classpath:application.properties")
@ConfigurationProperties(prefix = "vals")
public class ApplicationProperties
{
  public String getGtslEndpoint()
  {
    return gtslEndpoint;
  }

  public void setGtslEndpoint(String gtslEndpoint)
  {
    this.gtslEndpoint = gtslEndpoint;
  }

  private String gtslEndpoint;


}
